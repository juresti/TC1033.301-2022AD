#include <iostream>
#include <cmath>
using namespace std; //evita uso de std::cout

// Demostración de uso de tipos numéricos
int main(){

    cout << (2+3*4*5) << endl;
    cout << (2+3)*5 << endl;
    cout << pow(2, 5) << endl;//Elevar potencia
    cout << float(9)/3 << endl;//División flotante
    cout << float(5)/3 << endl;
    cout << 11/3 << endl; //Resultado es int
    cout << 11%3 << endl; //Módulo normal
    cout << float(4)/6 << endl;
    cout << 4/6 << endl; //parte entera
    cout << 4%6 << endl; //residuo
    cout << pow(2, 150) << endl; //científica

    return 0;
}
